  @Override
  public void periodic() {
    m_poseBuffer.addSample(MathSharedStore.getTimestamp(), new InterpolationRecordPose(mPoseSupplier.get()));

    PhotonPipelineResult result = gamePieceCamera.getLatestResult();

    if(result.hasTargets())
    {
      List<Translation2d> gamePieceCorners = new ArrayList<>();
      List<Translation2d> gamePieceBoundingCorners = new ArrayList<>();
      List<Double> gamePieceWidths = new ArrayList<>();
      mGamePieceEstimatedPoses.clear();

      result.targets.forEach(y -> {
        
        List<Translation2d> translations = y.getMinAreaRectCorners().stream().map(x -> new Translation2d(x.x, x.y)).toList();
        gamePieceCorners.addAll(translations);
        Polygon gamePiecePoly = new Polygon();
        translations.forEach(x -> gamePiecePoly.addPoint((int)x.getX(), (int)x.getY()));
  
        Rectangle gamePieceRectangle = gamePiecePoly.getBounds();
        gamePieceBoundingCorners.add(new Translation2d(gamePieceRectangle.getX(), gamePieceRectangle.getY()));//top left
        gamePieceBoundingCorners.add(new Translation2d(gamePieceRectangle.getMaxX(), gamePieceRectangle.getY()));//top right
        gamePieceBoundingCorners.add(new Translation2d(gamePieceRectangle.getX(), gamePieceRectangle.getMaxY()));//bottom left
        gamePieceBoundingCorners.add(new Translation2d(gamePieceRectangle.getMaxX(), gamePieceRectangle.getMaxY()));
  
        gamePieceWidths.add(gamePieceRectangle.getWidth());
        // Logger.recordOutput("Vision/GamePieceCameraDistToMiddleNote", new Pose3d(mDrivetrainSubsystem.getPose()).plus(cameraToRobot).getTranslation().getDistance(middleNote));
        if(gamePieceRectangle.getWidth() >= MIN_GAME_PIECE_WIDTH && y.getYaw() < SIDE_YAW_DEGREES)
        {
          double centerDist = gamePieceWidthToDistMapCenter.get(gamePieceRectangle.getWidth());
          double sideDist = gamePieceWidthToDistMapSide.get(gamePieceRectangle.getWidth());
          double yawCompensatedDist = MathUtil.interpolate(centerDist, sideDist, Math.abs(y.getYaw()/SIDE_YAW_DEGREES));
          
          Pose3d estimatedPose3d = new Pose3d(m_poseBuffer.getSample(result.getTimestampSeconds() - kLatencyAddition).get().poseMeters)
              .plus(cameraToRobot)
              .plus(
                new Transform3d(
                  new Translation3d(yawCompensatedDist, new Rotation3d(0, -Math.toRadians(y.getPitch()), -Math.toRadians(y.getYaw()))), 
                  new Rotation3d()
                  )
              );
          estimatedPose3d = new Pose3d(estimatedPose3d.getX(), estimatedPose3d.getY(), Units.inchesToMeters(1), new Rotation3d());
          mGamePieceEstimatedPoses.add(estimatedPose3d);     
        }
        else
        {
          mGamePieceEstimatedPoses.add(new Pose3d());
        }
      });
              
      Logger.recordOutput("Vision/GamePieceWidths", gamePieceWidths.stream().mapToDouble(x -> x).toArray());
      Logger.recordOutput("Vision/GamePieceBoundingCorners", gamePieceBoundingCorners.toArray(Translation2d[]::new));
      Logger.recordOutput("Vision/GamePieceCorners", gamePieceCorners.toArray(Translation2d[]::new));    
      Logger.recordOutput("Vision/GamePieceEstimatedLocation", mGamePieceEstimatedPoses.toArray(Pose3d[]::new));
    }
    else
    {
      Logger.recordOutput("Vision/GamePieceWidths", new double[]{});
      Logger.recordOutput("Vision/GamePieceBoundingCorners", new Translation2d[]{});
      Logger.recordOutput("Vision/GamePieceCorners", new Translation2d[]{});    
      Logger.recordOutput("Vision/GamePieceEstimatedLocation", new Pose3d[]{});
    }
    
    Logger.recordOutput("Vision/GamePieceCameraLocation", new Pose3d(mPoseSupplier.get()).plus(cameraToRobot));
  }

  public List<Pose3d> getEstimatedGamePieceLocations()
  {
    mGamePieceEstimatedPoses.removeIf(x -> x == new Pose3d());//remove the detections that are outside of our interpolation table
    return mGamePieceEstimatedPoses;
  }
